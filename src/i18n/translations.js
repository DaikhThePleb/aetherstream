const translations = Object.fromEntries(
    APP_LANGUAGE_OPTIONS.map(({ value }) => {
        const base = BASE_TRANSLATIONS[value] || {};
        const extra = EXTRA_TRANSLATIONS[value] || {};
        return [value, { ...base, ...extra }];
    }),
);

export function translate(lang, key, fallback = '') {
    const safeLang = translations[lang] ? lang : FALLBACK_LANG;
    const value = translations[safeLang]?.[key];
    if (value !== undefined) return value;

    const fallbackValue = translations[FALLBACK_LANG]?.[key];
    if (fallbackValue !== undefined) return fallbackValue;

    return fallback || key;
}

export function createTranslator(lang) {
    return (key, fallback = '') => translate(lang, key, fallback);
}

export { translations as TRANSLATIONS };